export const getInitialLanguage = (): LanguageCode => {
  if (typeof navigator !== 'undefined') {
    const browserLang = navigator.language.split('-')[0] as LanguageCode;
    if (SUPPORTED_LANGUAGES[browserLang]) {
      return browserLang;
    }
  }
  return DEFAULT_LANGUAGE;
};
